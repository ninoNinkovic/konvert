<h2>What is KONVERT?</h2>
KONVERT is a online conversion tool for <i>Avid</i> .ale and <i>Final Cut</i> .txt log-files.
It converts .ale to .txt and .txt to .ale.

<h2>How does KONVERT work?</h2>
On the home screen simply drag'n'drop your .ale (Avid) or .txt (Final Cut) file on the green box labeled with "DROP HERE".
After a few seconds a button appears to download your converted file.
<br>
<h2>Is KONVERT free?</h2>
KONVERT is a free online service and open source. You are also very welcome to contribute code or refine/improve existing code.
<br>
<h2>Is KONVERT safe?</h2>
KONVERT was designed for a user-base of 1 person working with a very fast intranet. We made a few ugly patches to make KONVERT work in the vast domain of the internet. A lot of improvement can be done here. <br>You can download the sources and set up your own server if you do not trust this site.
<br>
<h2>Who made KONVERT and why?</h2>
KONVERT emerged out of the need for a simple conversion tool. It was designed as an simple but easy to use in-house tool. It works cool for us, so we thought, it maybe works cool for you as well. 
